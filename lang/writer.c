#include "lang/lang.h"

static StringBuf* sb_contents;
static StringBuf* sb_header;
static StringBuf* sb_init1a;
static StringBuf* sb_init1b;
static StringBuf* sb_init2;
static StringBuf* sb_init3;

void wr_init()
{
  sb_contents = mem_new(StringBuf);
  sb_header = mem_new(StringBuf);
  sb_init1a = mem_new(StringBuf);
  sb_init1b = mem_new(StringBuf);
  sb_init2 = mem_new(StringBuf);
  sb_init3 = mem_new(StringBuf);
  sbuf_init(sb_contents, "");
  sbuf_init(sb_header, "");
  sbuf_init(sb_init1a, "");
  sbuf_init(sb_init1b, "");
  sbuf_init(sb_init2, "");
  sbuf_init(sb_init3, "");
}

void wr_print(int destination, const char* format, ...)
{
  va_list ap;
  va_start(ap, format);
  // Calculate needed buffer
  int sz = vsnprintf(NULL, 0, format, ap) + 1;
  va_end(ap);

  char tmpbuf[sz];
  va_start(ap, format);
  vsnprintf(tmpbuf, sz, format, ap);
  tmpbuf[sz - 1] = 0;
  va_end(ap);

  StringBuf* dest = NULL;
  switch(destination){
    case WR_INIT1A:
      dest = sb_init1a;
      break;
    case WR_INIT1B:
      dest = sb_init1b;
      break;
    case WR_INIT2:
      dest = sb_init2;
      break;
    case WR_INIT3:
      dest = sb_init3;
      break;
    case WR_CODE:
      dest = sb_contents;
      break;
    case WR_HEADER:
      dest = sb_header;
      break;
    default:
      assert_never();
  }

  sbuf_cat(dest, tmpbuf);
}

void wr_dump(FILE* f, const char* module_name)
{
  fprintf(f, "// %s.c -- Autogenerated by Ripec\n", module_name);
  fprintf(f, "#include \"modules/modules.h\"\n");
  fprintf(f, "%s", sb_header->str);
  fprintf(f, "%s", sb_contents->str);

  fprintf(f, "void init1a_%s(){\n", module_name);
  fprintf(f, "%s", sb_init1a->str);
  fprintf(f, "}\n");

  fprintf(f, "void init1b_%s(){\n", module_name);
  fprintf(f, "%s", sb_init1b->str);
  fprintf(f, "}\n");

  fprintf(f, "void init2_%s(){\n", module_name);
  fprintf(f, "%s", sb_init2->str);
  fprintf(f, "}\n");

  fprintf(f, "void init3_%s(){\n", module_name);
  fprintf(f, "%s", sb_init3->str);
  fprintf(f, "}\n");
}
